let letter_to_morse = function
  |'a' | 'A' -> ['.' ; '-']
  |'b' | 'B' -> ['-' ; '.' ; '.' ; '.']
  |'c' | 'C' -> ['-' ; '.' ; '-' ; '.']
  |'d' | 'D' -> ['-' ; '.' ; '.']
  |'e' | 'E' -> ['.']
  |'f' | 'F' -> ['.' ; '.' ; '-' ; '.'] 
  |'g' | 'G' -> ['-' ; '-' ; '.']
  |'h' | 'H' -> ['.' ; '.' ; '.' ; '.']
  |'i' | 'I' -> ['.'; '.']
  |'j' | 'J' -> ['.' ; '-' ; '-' ; '-']
  |'k' | 'K' -> ['-' ; '.' ; '-']
  |'l' | 'L' -> ['.' ; '-' ; '.' ; '.']
  |'m' | 'M' -> ['-' ; '-']
  |'n' | 'N' -> ['-' ; '.']
  |'o' | 'O' -> ['-' ; '-' ; '-']
  |'p' | 'P' -> ['.' ; '-' ; '-' ; '.']
  |'q' | 'Q' -> ['-' ; '-' ; '.' ; '-']
  |'r' | 'R' -> ['.' ; '-' ; '.']
  |'s' | 'S' -> ['.' ; '.' ; '.']
  |'t' | 'T' -> ['-']
  |'u' | 'U' -> ['.' ; '.' ; '-']
  |'v' | 'V' -> ['.' ; '.' ; '.' ; '-']
  |'w' | 'W' -> ['.' ; '-' ; '-']
  |'x' | 'X' -> ['-' ; '.' ; '.' ; '-']
  |'y' | 'Y' -> ['-' ; '.' ; '-' ; '-']
  |'z' | 'Z' -> ['-' ; '-' ; '.' ; '.']
  | _  -> failwith "The type of your input is not a char, please try with another data";;

